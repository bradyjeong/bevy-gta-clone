fn spawn_unified_tree(
    commands: &mut Commands,
    chunk_coord: ChunkCoord,
    position: Vec3,
    meshes: &mut ResMut<Assets<Mesh>>,
    materials: &mut ResMut<Assets<StandardMaterial>>,
) -> Entity {
    // Create meshes for different LOD levels (palm tree specific)
    let full_mesh = meshes.add(create_detailed_palm_mesh());
    let medium_mesh = meshes.add(create_medium_palm_mesh());
    let billboard_mesh = meshes.add(create_billboard_quad());
    
    let tree_entity = commands.spawn((
        UnifiedChunkEntity {
            coord: chunk_coord,
            layer: ContentLayer::Vegetation,
        },
        Transform::from_xyz(position.x, 0.0, position.z),
        Visibility::default(),
        InheritedVisibility::VISIBLE,
        ViewVisibility::default(),
        Cullable { max_distance: 300.0, is_culled: false }, // Extended range for billboard LOD
        DynamicContent {
            content_type: ContentType::Tree,
        },
        VegetationLOD::new(),
        VegetationMeshLOD::new(
            full_mesh.clone(),
            Some(medium_mesh),
            billboard_mesh,
        ),
        VegetationBillboard::new(
            Vec3::new(1.0, 3.0, 1.0), // Palm tree proportions
            Vec2::new(2.0, 3.0),
        ),
        Mesh3d(full_mesh), // Start with full detail mesh
    )).id();
    
    // Palm tree trunk - single brown cylinder
    commands.spawn((
        Mesh3d(meshes.add(Cylinder::new(0.3, 8.0))),
        MeshMaterial3d(materials.add(Color::srgb(0.4, 0.25, 0.15))), // Brown trunk
        Transform::from_xyz(0.0, 4.0, 0.0),
        ChildOf(tree_entity),
        VisibleChildBundle::default(),
    ));

    // Palm fronds - 4 green rectangles arranged in a cross
    for i in 0..4 {
        let angle = (i as f32) * std::f32::consts::PI / 2.0;
        
        commands.spawn((
            Mesh3d(meshes.add(Cuboid::new(2.5, 0.1, 0.8))),
            MeshMaterial3d(materials.add(Color::srgb(0.2, 0.6, 0.25))), // Green fronds
            Transform::from_xyz(
                angle.cos() * 1.2, 
                7.5, 
                angle.sin() * 1.2
            ).with_rotation(
                Quat::from_rotation_y(angle) * 
                Quat::from_rotation_z(-0.2) // Slight droop
            ),
            ChildOf(tree_entity),
            VisibleChildBundle::default(),
        ));
    }

    // Physics collider for palm trunk
    commands.spawn((
        RigidBody::Fixed,
        Collider::cylinder(4.0, 0.3),
        CollisionGroups::new(STATIC_GROUP, Group::ALL),
        Transform::from_xyz(0.0, 4.0, 0.0),
        ChildOf(tree_entity),
    ));
    
    tree_entity
}

// Utility functions

fn is_on_road_unified(position: Vec3, road_network: &RoadNetwork) -> bool {
    for road in road_network.roads.values() {
        if is_point_on_road_spline_unified(position, road, 25.0) {
            return true;
        }
    }
    false
}

fn is_point_on_road_spline_unified(position: Vec3, road: &RoadSpline, tolerance: f32) -> bool {
    let samples = 20; // Reduced samples for performance
    let width = road.road_type.width();
    
    for i in 0..samples {
        let t = i as f32 / (samples - 1) as f32;
        let road_point = road.evaluate(t);
        let distance = Vec3::new(position.x - road_point.x, 0.0, position.z - road_point.z).length();
        
        if distance <= width * 0.5 + tolerance {
            return true;
        }
    }
    
    false
}

fn create_road_material(road_type: &RoadType, materials: &mut ResMut<Assets<StandardMaterial>>) -> Handle<StandardMaterial> {
    let (base_color, roughness) = match road_type {
        RoadType::Highway => (Color::srgb(0.4, 0.4, 0.45), 0.8),
        RoadType::MainStreet => (Color::srgb(0.35, 0.35, 0.4), 0.8),
        RoadType::SideStreet => (Color::srgb(0.45, 0.45, 0.5), 0.7),
        RoadType::Alley => (Color::srgb(0.5, 0.5, 0.45), 0.6),
    };
    
    materials.add(StandardMaterial {
        base_color,
        perceptual_roughness: roughness,
        metallic: 0.0,
        reflectance: 0.2,
        emissive: Color::BLACK.into(),
        ..default()
    })
}

fn create_marking_material(materials: &mut ResMut<Assets<StandardMaterial>>) -> Handle<StandardMaterial> {
    materials.add(StandardMaterial {
        base_color: Color::srgb(0.95, 0.95, 0.95),
        emissive: LinearRgba::new(0.2, 0.2, 0.2, 1.0),
        perceptual_roughness: 0.6,
        metallic: 0.0,
        reflectance: 0.5,
        ..default()
    })
}

